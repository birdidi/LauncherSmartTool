    public static String[] getFuzzyRegexs(String... keyword) {
        if(keyword == null || keyword.length == 0){
            return keyword;
        }

        String[] res = new String[keyword.length];
        for (int i = 0, len = keyword.length; i < len; i++) {
            res[i] = "[\\S\\s]*" + keyword[i] + "[\\s\\S]*";
        }
        return res;
    }
